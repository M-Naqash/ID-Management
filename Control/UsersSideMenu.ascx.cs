using System;
using System.Collections.Generic;
using System.Linq;
using System.Web;
using System.Web.UI;
using System.Web.UI.WebControls;

public partial class UsersSideMenu : System.Web.UI.UserControl
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected void Page_Load(object sender, EventArgs e)
    {
        FormSession.FillSession("",null);
        PermSideMenu();
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    protected void btnSideMenu_Click(object sender, EventArgs e)
    {
        LinkButton lb = (LinkButton)sender;
        Response.Redirect(lb.PostBackUrl);
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    private void PermSideMenu()
    {
        //btnUpdateUsers.Enabled = true;

        btnCreateUsers.Enabled = FormSession.PermUsr.Contains("IUsr");
        btnUpdateUsers.Enabled = FormSession.PermUsr.Contains("UUsr");
        btnDeleteUsers.Enabled = FormSession.PermUsr.Contains("DUsr");

        btnSearchUsers.Enabled = FormSession.PermUsr.Contains("SUsr");

        btnCreateRole.Enabled = FormSession.PermUsr.Contains("IUsr");
        btnUpdateRole.Enabled = FormSession.PermUsr.Contains("UUsr");
        btnDeleteRole.Enabled = FormSession.PermUsr.Contains("DUsr");
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void RedirectPage()
    {
        FormSession.FillSession("",null);
        PermSideMenu();
        foreach (Control ctrl in this.Controls)
        {
            if (ctrl is LinkButton) 
            {  
                LinkButton lb = (LinkButton)ctrl;
                string id = lb.ID; 
                if (lb.Enabled) { Response.Redirect(lb.PostBackUrl); break; }
            }
        }
    }
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}